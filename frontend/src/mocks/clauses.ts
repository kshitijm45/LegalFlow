export const getClauseSummary = (categories: ClauseCategory[]) => {
  const allClauses = categories.flatMap((c) => c.clauses)
  return {
    total: allClauses.length,
    present: allClauses.filter((c) => c.status === 'present').length,
    missing: allClauses.filter((c) => c.status === 'missing').length,
    partial: allClauses.filter((c) => c.status === 'partial').length,
    coverage: Math.round(
      (allClauses.filter((c) => c.status === 'present').length / allClauses.length) * 100
    ),
  }
}
